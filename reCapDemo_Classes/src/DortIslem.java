public class DortIslem {
    public int Topla(int say1, int say2) {
        return say1+say2;
    }
    public int Cikar(int say1, int say2) {
        return say1-say2;
    }
    public int Carp(int say1, int say2) {
        return say1*say2;
    }
    public int Bol(int say1, int say2) {
        return say1/say2;
    }
}
